// Sort by prefix length descending for longest-match-first
const SORTED = [...PHONE_PREFIXES].sort((a, b) => b[0].length - a[0].length);

export function detectCountryFromPhone(phone: string | null | undefined): { code: string; flag: string } | null {
  if (!phone) return null;
  // Normalize: strip spaces, dashes, parens
  const normalized = phone.replace(/[\s\-()]/g, '');
  // Ensure starts with +; also handle raw numbers starting with 1 (North America)
  const withPlus = normalized.startsWith('+') ? normalized : (normalized.startsWith('00') ? '+' + normalized.slice(2) : (normalized.startsWith('1') && normalized.length >= 10 ? '+' + normalized : null));
  if (!withPlus) return null;

  for (const [prefix, code, flag] of SORTED) {
    if (withPlus.startsWith(prefix)) {
      // Special case: +1 could be US or CA — we can't distinguish without more info, show US
      return { code, flag };
    }
  }
  return null;
}
